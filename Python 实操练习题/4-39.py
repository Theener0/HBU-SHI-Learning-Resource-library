"""
小明拿100元去买50支笔，已知钢笔10元一支，圆珠笔3元一支，铅笔1元一支，请编程算出所有可能的组合方式。
输出格式如下所示：
钢笔0支,圆珠笔25支,铅笔25支
钢笔2支,圆珠笔16支,铅笔32支
......
"""
#x钢笔,y圆珠笔,z铅笔

for x in range(0,50):
    for y in range(0,50):
        for z in range(0,50):
            if x+y+z==50 and x*10+y*3+z*1==100:
                print("钢笔%d支,圆珠笔%d支,铅笔%d支"%(x,y,z))

